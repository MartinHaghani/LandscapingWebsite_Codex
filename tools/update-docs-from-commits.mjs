import { execSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const CHANGELOG_PATH = resolve(process.cwd(), 'docs', 'changelog.md');

const raw = execSync('git log --date=short --pretty=format:%h%x09%ad%x09%s -n 100', {
  encoding: 'utf8'
}).trim();

const commits = raw
  .split('\n')
  .filter(Boolean)
  .map((line) => {
    const [hash, date, ...subjectParts] = line.split('\t');
    return {
      hash,
      date,
      subject: subjectParts.join('\t').trim()
    };
  })
  .filter(({ subject }) => {
    return !subject.toLowerCase().startsWith('docs: automated changelog update');
  });

const groupedByDate = commits.reduce((acc, commit) => {
  if (!acc.has(commit.date)) {
    acc.set(commit.date, []);
  }
  acc.get(commit.date).push(commit);
  return acc;
}, new Map());

const lines = [
  '# Changelog',
  '',
  'This file is generated by `npm run docs:update` from git commit history.',
  ''
];

if (commits.length === 0) {
  lines.push('- No commits available.');
} else {
  for (const [date, entries] of groupedByDate.entries()) {
    lines.push(`## ${date}`);
    lines.push('');
    for (const entry of entries) {
      lines.push(`- \`${entry.hash}\` ${entry.subject}`);
    }
    lines.push('');
  }
}

mkdirSync(resolve(process.cwd(), 'docs'), { recursive: true });
writeFileSync(CHANGELOG_PATH, `${lines.join('\n').trim()}\n`, 'utf8');
